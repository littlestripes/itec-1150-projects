# Print #s from 0 to 5
print("These are the numbers from 1 to 5.")
for i in range(6):
    print(i)

# Print #s from 1 to 20
print("These are the numbers from 1 to 20.")
for i in range(21):
    print(i)

# Print even #s from 0 to 24
print("These are the even numbers from 0 to 24.")
for i in range(0, 25, 2):
    print(i)

# Print odd #s from 37 to 53
print("These are the odd numbers from 37 to 53.")
for i in range(37, 54, 2):
    print(i)

# Print 10s to 60
print("This counts from 10 to 60 by 10s.")
for i in range(10, 61, 10):
    print(i)

# Print #s counting down from 30 to 20
print("This counts down from 30 to 20.")
for i in range(30, 19, -1):
    print(i)

